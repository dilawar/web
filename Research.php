	<h4> Interests </h4>

  As far as my graduate studies is concerned, I have been working on and
  learning algorithm design. I was trained as a VLSI designer, and my work is
  mostly in the field of VLSI CAD.  <b>Mathematics</b> and <b>Social
      Anthropology</b> are my hobbies and I spend a fair amount of time learning
  these subjects.  <br><br> I easily	get curious about people, especially when
  they act as a group. My interests in anthropology are limited to middle classes
  and their institutes simply because I live among them. I also have 	a very
  soft corner for Indian History. Irfan Habib, Ranajit Guha, D. D. Kosambi  and
  Ramachandra Guha are my favorite historian. I love reading and writing essays.
  Andre Beteille and Ramachandra Guha are my favorite writers. P. Sainath and T.
  Ramachandran are my favorite journalists. And I hate <i>Times of India</i>.
	
	<br><br> 
	
	I do not like fiction much unless written by grass-root writers. I
